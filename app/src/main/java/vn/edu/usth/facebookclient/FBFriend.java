package vn.edu.usth.facebookclient;

public class FBFriend {
    private String id;
    private String name;

    public FBFriend(String id, String name) {
        this.name = name;
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
